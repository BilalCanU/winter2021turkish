package day03;

public class C6_IncrementDecrement {

	public static void main(String[] args) {
	
		int number=10;
		// Bir variable'ın değerini toplayarak artırmak için 3 yöntem kullanabilirim
		
		
		// 1. yöntem (Acemi yöntemi)
		
		number = number +5; // num =15
		
		// 2. yöntem
		
		number +=8; //  num =23
		
		// 3. yöntem sadece 1 (bir) artırmak için
		
		number++; // num =24
		
		// bu yöntemler sadece toplama işlemi has değildir tüm operatörler için kullanılabilir
		// 3. yöntem sadece toplama ve çıkartma için geçerlidir.
		
		// number'ı 6 eksiltin
		
		number -=6; // num=18
		
		// number'ı ikiye bölün
		
		number /= 2; // number= 9
		
		// number'ı 3'le çarpın
		
		number *= 3; // number= 27
		
		// number'ı 1 eksiltin
		
		number --; // number= 26
		
		
		
		
	}
	
	
	
	
}
